    public void mostrarOpcionesModificar(){
        //mostrando text fields
        txt_nombre.setVisible(true);
        txt_numCuenta.setVisible(true);
        txt_contacto.setVisible(true);
        txt_carrera.setVisible(true);
        //bloquear el numero de cuenta para que no podamos editar el valor que hay dentro. 
        txt_numCuenta.setEnabled(false);
        // ocultando botones para otras opciones
        btn_eliminar.setVisible(false);
        btn_agregar.setVisible(false);
        btn_listar.setVisible(false);
        btn_modificar.setVisible(true);
        
        lbl_nombre.setVisible(true);
        lbl_numCuenta.setVisible(true);
        lbl_contacto.setVisible(true);
        lbl_carrera.setVisible(true);
    }
    public void mostrarOpcionesAgregar(){
        //mostrando text fields
        txt_nombre.setVisible(true);
        txt_numCuenta.setVisible(true);
        txt_contacto.setVisible(true);
        txt_carrera.setVisible(true);
        //habilitar el textfield de numero de cuenta 
        txt_numCuenta.setEnabled(true);
        // ocultando botones para otras opciones
        btn_eliminar.setVisible(false);
        btn_modificar.setVisible(false);
        btn_listar.setVisible(false);
        btn_agregar.setVisible(true);
        
        lbl_nombre.setVisible(true);
        lbl_numCuenta.setVisible(true);
        lbl_contacto.setVisible(true);
        lbl_carrera.setVisible(true);
    }
